public class Point{

  private double x;
  private double y;

  public Point()
  {
    x = 0;
    y = 0;
  }
  public Point(double xPos, double yPos)
  {
    x = xPos;
    y = yPos;
  }
  public double getX()
  {
    return x;
  }
  public double getY()
  {
    return y;
  }
}
